    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //
        $request->validate([

            'facebook' => 'required|string|min:3|max:45',
            'twitter' => 'required|string|min:3|max:45',
            'linkedin' => 'required|string|min:3|max:45',
            'whatsapp' => 'required|string|min:3|max:45',


        ]);

            $item =new Social();
            $item->facebook = $request->input('facebook');
            $item->twitter = $request->input('twitter');
            $item->linkedin = $request->input('linkedin');
            $item->whatsapp = $request->input('whatsapp');

           $item->save();

        return redirect()->back();

    }

